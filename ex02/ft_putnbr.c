#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int num;

	num = nb;
	if (nb < 0)
	{
		num *= -1;
		write(1, "-", 1);
	}
	if (num >= 10)
		ft_putnbr(num / 10);
	num = num % 10 + 48;
	write(1, &num, 1);
}
